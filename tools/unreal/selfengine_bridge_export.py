def _component_world_bounds(component: Any) -> Optional[Tuple[List[float], List[float]]]:
    try:
        box = component.bounds.get_box()
        minimum = _vector(box.min)
        maximum = _vector(box.max)
        if max(abs(maximum[index] - minimum[index]) for index in range(3)) > 0.1:
            return minimum, maximum
    except Exception:
        pass

    try:
        origin = component.bounds.origin
        extent = component.bounds.box_extent
        minimum = [
            float(getattr(origin, "x", 0.0)) - float(getattr(extent, "x", 0.0)),
            float(getattr(origin, "y", 0.0)) - float(getattr(extent, "y", 0.0)),
            float(getattr(origin, "z", 0.0)) - float(getattr(extent, "z", 0.0)),
        ]
        maximum = [
            float(getattr(origin, "x", 0.0)) + float(getattr(extent, "x", 0.0)),
            float(getattr(origin, "y", 0.0)) + float(getattr(extent, "y", 0.0)),
            float(getattr(origin, "z", 0.0)) + float(getattr(extent, "z", 0.0)),
        ]
        if max(abs(maximum[index] - minimum[index]) for index in range(3)) > 0.1:
            return minimum, maximum
    except Exception:
        pass

    try:
        origin, extent = component.get_local_bounds()
        transform = component.get_world_transform()
        corners: List[List[float]] = []
        for x_sign in (-1.0, 1.0):
            for y_sign in (-1.0, 1.0):
                for z_sign in (-1.0, 1.0):
                    local = unreal.Vector(
                        float(getattr(origin, "x", 0.0)) + float(getattr(extent, "x", 0.0)) * x_sign,
                        float(getattr(origin, "y", 0.0)) + float(getattr(extent, "y", 0.0)) * y_sign,
                        float(getattr(origin, "z", 0.0)) + float(getattr(extent, "z", 0.0)) * z_sign,
                    )
                    world = transform.transform_position(local)
                    corners.append(_vector(world))
        return (
            [min(corner[index] for corner in corners) for index in range(3)],
            [max(corner[index] for corner in corners) for index in range(3)],
        )
    except Exception:
        return None


def _mesh_component_bounds_for_camera(
    component: Any,
    source_asset_id: str,
    mesh_record: Dict[str, Any]
) -> Optional[Tuple[List[float], List[float]]]:
    if "SkySphere" in source_asset_id or "EngineSky" in source_asset_id:
        return None

    bounds = _component_world_bounds(component)
    if bounds is None:
        local_bounds = mesh_record.get("bounds") or {}
        transform = mesh_record.get("transform") or {}
        local_min = local_bounds.get("localMin")
        local_max = local_bounds.get("localMax")
        position = transform.get("position")
        scale = transform.get("scale")
        if (
            isinstance(local_min, list) and len(local_min) >= 3 and
            isinstance(local_max, list) and len(local_max) >= 3 and
            isinstance(position, list) and len(position) >= 3 and
            isinstance(scale, list) and len(scale) >= 3
        ):
            minimum = [
                float(position[index]) + min(float(local_min[index]) * float(scale[index]), float(local_max[index]) * float(scale[index]))
                for index in range(3)
            ]
            maximum = [
                float(position[index]) + max(float(local_min[index]) * float(scale[index]), float(local_max[index]) * float(scale[index]))
                for index in range(3)
            ]
            bounds = (minimum, maximum)
        else:
            return None

    minimum, maximum = bounds
    extent = _vec_mul(_vec_sub(maximum, minimum), 0.5)
    if max(abs(extent[0]), abs(extent[1]), abs(extent[2])) <= 0.1:
        return None
    return bounds


def _merge_bounds(
    current: Optional[Tuple[List[float], List[float]]],
    candidate: Tuple[List[float], List[float]]
) -> Tuple[List[float], List[float]]:
    if current is None:
        return ([candidate[0][0], candidate[0][1], candidate[0][2]], [candidate[1][0], candidate[1][1], candidate[1][2]])
    return (
        [min(current[0][index], candidate[0][index]) for index in range(3)],
        [max(current[1][index], candidate[1][index]) for index in range(3)],
    )


def _generated_reference_camera_record(
    scene: Dict[str, Any],
    mesh_bounds: Optional[Tuple[List[float], List[float]]],
    fallback_location: List[float]
) -> Dict[str, Any]:
    if mesh_bounds is not None:
        minimum, maximum = mesh_bounds
        center = _vec_mid(minimum, maximum)
        extent = _vec_mul(_vec_sub(maximum, minimum), 0.5)
        radius = max(_vec_length(extent), 250.0)
    else:
        center = fallback_location
        radius = 1000.0

    view_direction = _vec_normalize([-0.62, -0.56, -0.55], [-1.0, -0.35, -0.35])
    distance = max(radius * 2.7, 900.0)
    position = _vec_sub(center, _vec_mul(view_direction, distance))
    position[2] += max(radius * 0.35, 180.0)
    forward = _vec_normalize(_vec_sub(center, position), view_direction)
    up = [0.0, 0.0, 1.0]

    record = {
        "id": f"{scene.get('id', 'scene')}:GeneratedReferenceCamera",
        "actorId": "",
        "actorName": "GeneratedReferenceCamera",
        "componentName": "GeneratedReferenceCamera",
        "componentClass": "SelfEngineGeneratedReferenceCamera",
        "source": "selfengine_bridge_generated_from_mesh_bounds",
        "transform": {
            "position": position,
            "rotationDegrees": [0.0, 0.0, 0.0],
            "forward": forward,
            "up": up,
            "target": center,
        },
        "fieldOfView": 60.0,
        "aspectRatio": 16.0 / 9.0,
        "nearClipPlane": 1.0,
        "target": center,
        "boundsRadius": radius,
        "generationMessage": "Generated because the UE level contains no CameraComponent; replace with a captured UE viewport bookmark when available.",
    }
    return record


