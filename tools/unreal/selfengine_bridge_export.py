def _upsert_reference_capture(
    scene: Dict[str, Any],
    record: Dict[str, Any]
) -> None:
    record_id = str(record.get("id") or "")
    existing = list(scene.get("referenceCaptures") or [])
    filtered = [
        item for item in existing
        if str(item.get("id") or "") != record_id
    ]
    filtered.append(record)
    scene["referenceCaptures"] = filtered


def _post_process_record(actor: Any) -> Optional[Dict[str, Any]]:
    class_name = _class_name(actor)
    if "PostProcess" not in class_name:
        return None

    settings = _safe_editor_property(actor, "settings", None)
    record: Dict[str, Any] = {
        "id": actor.get_path_name(),
        "actorName": _actor_label(actor),
        "class": class_name,
        "transform": _actor_record(actor)["transform"],
    }
    if settings is not None:
        for field in (
            "auto_exposure_bias",
            "bloom_intensity",
            "film_slope",
            "film_toe",
            "film_shoulder",
            "film_black_clip",
            "film_white_clip",
            "vignette_intensity",
            "motion_blur_amount",
        ):
            value = _safe_editor_property(settings, field, None)
            if isinstance(value, (int, float)) and math.isfinite(float(value)):
                record[field] = float(value)
    return record


def _load_manifest(path: str) -> Dict[str, Any]:
    with open(path, "r", encoding="utf-8-sig") as file:
        return json.load(file)


def _write_manifest(path: str, manifest: Dict[str, Any]) -> None:
    with open(path, "w", encoding="utf-8") as file:
        json.dump(manifest, file, ensure_ascii=False, indent=2)
        file.write("\n")


def _scene_map_path(scene: Dict[str, Any]) -> str:
    source_asset_id = str(scene.get("sourceAssetId") or scene.get("id") or "")
    if source_asset_id.startswith("/Game/"):
        return source_asset_id

    source_map_path = str(scene.get("sourceMapPath") or "")
    content_path = str(scene.get("contentPath") or "")
    if source_map_path.endswith(".umap"):
        stem = os.path.splitext(os.path.basename(source_map_path))[0]
        return "/Game/" + stem
    return source_asset_id


def _open_map(map_path: str) -> bool:
    if not map_path:
        return False
    try:
        return bool(unreal.EditorLoadingAndSavingUtils.load_map(map_path))
    except Exception as exc:
        _warn(f"Failed to open map {map_path}: {exc}")
        return False


def _all_level_actors() -> Iterable[Any]:
    try:
        return unreal.EditorLevelLibrary.get_all_level_actors()
    except Exception:
        return unreal.EditorActorSubsystem().get_all_level_actors()


def _actor_components(actor: Any) -> Iterable[Any]:
    try:
        return actor.get_components_by_class(unreal.ActorComponent)
    except Exception:
        try:
            return actor.get_components_by_class(unreal.SceneComponent)
        except Exception:
            return []


def _export_scene(scene: Dict[str, Any], context: Dict[str, Any]) -> Dict[str, Any]:
    map_path = _scene_map_path(scene)
    scene["ueEditorMapPath"] = map_path
    if not _open_map(map_path):
        scene["exportStatus"] = "ue_editor_map_open_failed"
        return scene

    actors: List[Dict[str, Any]] = []
    mesh_instances: List[Dict[str, Any]] = []
    lights: List[Dict[str, Any]] = []
    cameras: List[Dict[str, Any]] = []
    post_process_volumes: List[Dict[str, Any]] = []
    camera_mesh_bounds: Optional[Tuple[List[float], List[float]]] = None
    actor_fallback_locations: List[List[float]] = []

    for actor in _all_level_actors():
        if actor is None:
            continue
        actor_info = _actor_record(actor)
        actors.append(actor_info)
        transform_info = actor_info.get("transform", {})
        if isinstance(transform_info, dict):
            position_info = transform_info.get("position")
            if isinstance(position_info, list) and len(position_info) >= 3:
                actor_fallback_locations.append([
                    float(position_info[0]),
                    float(position_info[1]),
                    float(position_info[2]),
                ])
        post_process = _post_process_record(actor)
        if post_process is not None:
            post_process_volumes.append(post_process)

        for component in _actor_components(actor):
            if component is None:
                continue
            mesh_record = _mesh_component_record(actor, component, context)
            if mesh_record is not None:
                mesh_instances.append(mesh_record)
                bounds = _mesh_component_bounds_for_camera(
                    component,
                    str(mesh_record.get("sourceAssetId") or ""),
                    mesh_record
                )
                if bounds is not None:
                    camera_mesh_bounds = _merge_bounds(camera_mesh_bounds, bounds)
            light_record = _light_component_record(actor, component)
            if light_record is not None:
                lights.append(light_record)
            camera_record = _camera_component_record(actor, component)
            if camera_record is not None:
                cameras.append(camera_record)

    if not cameras:
        fallback_location = [0.0, 0.0, 0.0]
        if actor_fallback_locations:
            total = [0.0, 0.0, 0.0]
            for location in actor_fallback_locations:
                total = _vec_add(total, location)
            fallback_location = _vec_mul(total, 1.0 / float(len(actor_fallback_locations)))
        generated_camera = _generated_reference_camera_record(scene, camera_mesh_bounds, fallback_location)
        cameras.append(generated_camera)
        _upsert_reference_capture(scene, _reference_capture_record_from_camera(scene, generated_camera))
        _log(f"{map_path}: generated fallback reference camera from mesh bounds")

    scene["actors"] = actors
    scene["meshInstances"] = mesh_instances
    scene["lights"] = lights
    scene["cameras"] = cameras
    scene["postProcessVolumes"] = post_process_volumes
    scene["actorCount"] = len(actors)
    scene["meshInstanceCount"] = len(mesh_instances)
    scene["meshExportReadyCount"] = sum(
        1 for mesh in mesh_instances
        if str(mesh.get("exportedPath") or "") and os.path.isfile(str(mesh.get("exportedPath")))
    )
    scene["meshExportMissingCount"] = len(mesh_instances) - scene["meshExportReadyCount"]
    scene["lightCount"] = len(lights)
    scene["cameraCount"] = len(cameras)
    scene["generatedCameraCount"] = sum(
        1 for camera in cameras
        if str(camera.get("source") or "").startswith("selfengine_bridge_generated")
    )
    scene["postProcessVolumeCount"] = len(post_process_volumes)
    scene["referenceCaptureCount"] = len(scene.get("referenceCaptures") or [])
    scene["exportStatus"] = (
        "ue_editor_scene_ready_for_runtime"
        if mesh_instances and scene["meshExportMissingCount"] == 0
        else "ue_editor_metadata_exported"
    )
    scene["metadataExportedAtUtc"] = datetime.now(timezone.utc).isoformat()
    return scene


def _append_diagnostic(manifest: Dict[str, Any], message: str) -> None:
    diagnostics = manifest.setdefault("diagnostics", {})
    entries = diagnostics.setdefault("ueEditorAutomation", [])
    entries.append(
        {
            "timeUtc": datetime.now(timezone.utc).isoformat(),
            "message": message,
        }
    )


def parse_args(argv: List[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--manifest", default=os.environ.get("SELFENGINE_BRIDGE_MANIFEST", ""))
    parser.add_argument("--scene", default=os.environ.get("SELFENGINE_BRIDGE_SCENE", ""))
    parser.add_argument(
        "--max-scenes",
        type=int,
        default=int(os.environ.get("SELFENGINE_BRIDGE_MAX_SCENES", "0") or "0")
    )
    parser.add_argument("--no-mesh-export", action="store_true")
    parser.add_argument("--force-mesh-export", action="store_true")
    args = parser.parse_args(argv)
    args.no_mesh_export = args.no_mesh_export or (
        os.environ.get("SELFENGINE_BRIDGE_NO_MESH_EXPORT", "").lower() in ("1", "true", "yes", "on")
    )
    args.force_mesh_export = args.force_mesh_export or (
        os.environ.get("SELFENGINE_BRIDGE_FORCE_MESH_EXPORT", "").lower() in ("1", "true", "yes", "on")
    )
    if not args.manifest:
        parser.error("the following arguments are required: --manifest or SELFENGINE_BRIDGE_MANIFEST")
    return args


def main(argv: List[str]) -> int:
    args = parse_args(argv)
    manifest_path = os.path.abspath(args.manifest)
    manifest = _load_manifest(manifest_path)
    export_root = _default_export_root(manifest_path, manifest)
    manifest["exportedRootPath"] = export_root
    mesh_export_root = os.path.join(export_root, "Meshes")
    context: Dict[str, Any] = {
        "export_root": export_root,
        "mesh_export_root": mesh_export_root,
        "export_meshes": not bool(args.no_mesh_export),
        "force_mesh_export": bool(args.force_mesh_export),
        "mesh_export_cache": {},
    }
    scenes = list(manifest.get("scenes") or [])
    if args.scene:
        scenes = [
            scene for scene in scenes
            if args.scene in str(scene.get("id", "")) or args.scene in str(scene.get("name", ""))
        ]
    if args.max_scenes > 0:
        scenes = scenes[: args.max_scenes]

    if not scenes:
        _warn("No scenes selected for UE Editor bridge export")
        _append_diagnostic(manifest, "No scenes selected for UE Editor bridge export")
        _write_manifest(manifest_path, manifest)
        return 2

    _log(f"Exporting metadata for {len(scenes)} scene(s) into {manifest_path}")
    by_id = {str(scene.get("id")): scene for scene in manifest.get("scenes", [])}
    for scene in scenes:
        exported = _export_scene(scene, context)
        scene_id = str(exported.get("id"))
        by_id[scene_id] = exported
        _log(
            f"{scene_id}: actors={exported.get('actorCount', 0)} "
            f"meshes={exported.get('meshInstanceCount', 0)} "
            f"mesh_exports_ready={exported.get('meshExportReadyCount', 0)} "
            f"mesh_exports_missing={exported.get('meshExportMissingCount', 0)} "
            f"lights={exported.get('lightCount', 0)} "
            f"cameras={exported.get('cameraCount', 0)}"
        )

    manifest["scenes"] = list(by_id.values())
    mesh_exports = list(context["mesh_export_cache"].values())
    manifest["meshExports"] = mesh_exports
    manifest["ueEditorMetadataExportedAtUtc"] = datetime.now(timezone.utc).isoformat()
    manifest["ueEditorMeshExportedAtUtc"] = datetime.now(timezone.utc).isoformat()
    diagnostics = manifest.setdefault("diagnostics", {})
    diagnostics["needsUEEditorExportAutomation"] = any(
        scene.get("exportStatus") not in ("ue_editor_metadata_exported", "ue_editor_scene_ready_for_runtime")
        for scene in manifest.get("scenes", [])
    )
    diagnostics["ueEditorMeshExport"] = {
        "enabled": context["export_meshes"],
        "format": "fbx",
        "root": mesh_export_root,
        "uniqueMeshAssetCount": len(mesh_exports),
        "readyCount": sum(1 for item in mesh_exports if item.get("exportedPath")),
        "failedCount": sum(1 for item in mesh_exports if item.get("exportStatus") == "mesh_export_failed"),
        "missingCount": sum(1 for item in mesh_exports if not item.get("exportedPath")),
    }
    _append_diagnostic(
        manifest,
        f"UE Editor metadata export processed {len(scenes)} scene(s); "
        f"unique mesh assets={len(mesh_exports)}"
    )
    _write_manifest(manifest_path, manifest)
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
