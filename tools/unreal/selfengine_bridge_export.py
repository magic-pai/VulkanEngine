"""
Unreal Editor automation for SelfEngineBridge.json.

Run through UnrealEditor-Cmd.exe with:
  -ExecutePythonScript=".../tools/unreal/selfengine_bridge_export.py --manifest D:/UEProject/.../SelfEngineBridge.json"

This script intentionally complements the inventory manifest generated by
scripts/Generate-SelfEngineBridgeManifest.ps1. It runs inside UE, opens each map,
enumerates actors/components/lights/cameras/post-process volumes, and writes the
scene metadata back into the same bridge manifest.
"""

