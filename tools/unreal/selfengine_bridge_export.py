def _log(message: str) -> None:
    unreal.log("[SelfEngineBridge] " + message)


def _warn(message: str) -> None:
    unreal.log_warning("[SelfEngineBridge] " + message)


def _error(message: str) -> None:
    unreal.log_error("[SelfEngineBridge] " + message)


def _vector(value: Any) -> List[float]:
    return [
        float(getattr(value, "x", 0.0)),
        float(getattr(value, "y", 0.0)),
        float(getattr(value, "z", 0.0)),
    ]


def _rotator(value: Any) -> List[float]:
    return [
        float(getattr(value, "pitch", 0.0)),
        float(getattr(value, "yaw", 0.0)),
        float(getattr(value, "roll", 0.0)),
    ]


def _color(value: Any) -> List[float]:
    return [
        float(getattr(value, "r", 1.0)),
        float(getattr(value, "g", 1.0)),
        float(getattr(value, "b", 1.0)),
        float(getattr(value, "a", 1.0)),
    ]


def _vec_add(a: List[float], b: List[float]) -> List[float]:
    return [a[0] + b[0], a[1] + b[1], a[2] + b[2]]


def _vec_sub(a: List[float], b: List[float]) -> List[float]:
    return [a[0] - b[0], a[1] - b[1], a[2] - b[2]]


def _vec_mul(value: List[float], scalar: float) -> List[float]:
    return [value[0] * scalar, value[1] * scalar, value[2] * scalar]


def _vec_length(value: List[float]) -> float:
    return math.sqrt(value[0] * value[0] + value[1] * value[1] + value[2] * value[2])


def _vec_normalize(value: List[float], fallback: List[float]) -> List[float]:
    length = _vec_length(value)
    if length <= 0.0001:
        return fallback
    return [value[0] / length, value[1] / length, value[2] / length]


def _vec_mid(a: List[float], b: List[float]) -> List[float]:
    return [(a[0] + b[0]) * 0.5, (a[1] + b[1]) * 0.5, (a[2] + b[2]) * 0.5]


def _asset_path(value: Any) -> str:
    if value is None:
        return ""
    try:
        return str(value.get_path_name())
    except Exception:
        return str(value)


def _class_name(value: Any) -> str:
    try:
        return str(value.get_class().get_name())
    except Exception:
        return value.__class__.__name__


def _actor_label(actor: Any) -> str:
    try:
        return str(actor.get_actor_label())
    except Exception:
        try:
            return str(actor.get_name())
        except Exception:
            return ""


def _safe_editor_property(obj: Any, name: str, default: Any = None) -> Any:
    try:
        return obj.get_editor_property(name)
    except Exception:
        return default


def _set_editor_property(obj: Any, name: str, value: Any) -> bool:
    try:
        setattr(obj, name, value)
        return True
    except Exception:
        pass
    try:
        obj.set_editor_property(name, value)
        return True
    except Exception:
        return False


def _safe_file_stem(source_asset_id: str) -> str:
    text = source_asset_id.strip() or "unnamed_asset"
    digest = hashlib.sha1(text.encode("utf-8")).hexdigest()[:10]
    cleaned = re.sub(r"[^A-Za-z0-9._-]+", "_", text.strip("/").replace("/", "_"))
    cleaned = cleaned.strip("._-") or "unnamed_asset"
    if len(cleaned) > 96:
        cleaned = cleaned[-96:]
    return f"{cleaned}_{digest}"


def _asset_class_is_mesh(asset: Any) -> bool:
    class_name = _class_name(asset)
    return "StaticMesh" in class_name or "SkeletalMesh" in class_name


def _default_export_root(manifest_path: str, manifest: Dict[str, Any]) -> str:
    manifest_root = str(manifest.get("exportedRootPath") or "")
    if manifest_root:
        return os.path.abspath(manifest_root)

    project_root = str(manifest.get("projectRootPath") or "")
    if project_root:
        return os.path.abspath(os.path.join(project_root, "Saved", "SelfEngineBridge", "Exported"))

    return os.path.abspath(os.path.join(os.path.dirname(manifest_path), "Saved", "SelfEngineBridge", "Exported"))


def _create_fbx_export_options() -> Optional[Any]:
    try:
        options = unreal.FbxExportOption()
        _set_editor_property(options, "ascii", False)
        _set_editor_property(options, "collision", False)
        _set_editor_property(options, "level_of_detail", True)
        return options
    except Exception:
        return None


def _run_asset_export_task(asset: Any, output_path: str) -> Tuple[bool, str]:
    task = unreal.AssetExportTask()
    _set_editor_property(task, "object", asset)
    _set_editor_property(task, "filename", output_path)
    _set_editor_property(task, "automated", True)
    _set_editor_property(task, "prompt", False)
    _set_editor_property(task, "replace_identical", True)
    _set_editor_property(task, "write_empty_files", False)
    options = _create_fbx_export_options()
    if options is not None:
        _set_editor_property(task, "options", options)

    try:
        result = bool(unreal.Exporter.run_asset_export_task(task))
    except Exception as exc:
        return False, f"AssetExportTask failed: {exc}"

    if not result:
        return False, "AssetExportTask returned false"
    if not os.path.isfile(output_path):
        return False, "AssetExportTask reported success but no file was written"

    return True, ""


def _export_mesh_asset(source_mesh: Any, context: Dict[str, Any]) -> Dict[str, Any]:
    source_asset_id = _asset_path(source_mesh)
    if not source_asset_id:
        return {
            "sourceAssetId": "",
            "plannedExportedPath": "",
            "exportedPath": "",
            "exportStatus": "missing_source_mesh",
            "exportFormat": "fbx",
            "message": "Component had no source mesh asset.",
        }

    cache: Dict[str, Dict[str, Any]] = context["mesh_export_cache"]
    cached = cache.get(source_asset_id)
    if cached is not None:
        return cached

    output_path = os.path.join(
        context["mesh_export_root"],
        _safe_file_stem(source_asset_id) + ".fbx",
    )
    record: Dict[str, Any] = {
        "sourceAssetId": source_asset_id,
        "sourceAssetClass": _class_name(source_mesh),
        "plannedExportedPath": output_path,
        "exportedPath": "",
        "exportStatus": "requires_mesh_export",
        "exportFormat": "fbx",
        "message": "",
    }
    cache[source_asset_id] = record

    if not context["export_meshes"]:
        record["message"] = "Mesh export disabled by --no-mesh-export."
        return record

    if not _asset_class_is_mesh(source_mesh):
        record["exportStatus"] = "unsupported_mesh_asset_class"
        record["message"] = f"Unsupported source asset class: {_class_name(source_mesh)}"
        return record

    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    if os.path.isfile(output_path) and not context["force_mesh_export"]:
        record["exportedPath"] = output_path
        record["exportStatus"] = "exported_existing"
        return record

    ok, message = _run_asset_export_task(source_mesh, output_path)
    if ok:
        record["exportedPath"] = output_path
        record["exportStatus"] = "exported"
        return record

    record["exportStatus"] = "mesh_export_failed"
    record["message"] = message
    _warn(f"Mesh export failed for {source_asset_id}: {message}")
    return record


def _component_world_transform(component: Any) -> Dict[str, List[float]]:
    transform = component.get_world_transform()
    record = {
        "position": _vector(transform.translation),
        "rotationDegrees": _rotator(transform.rotation.rotator()),
        "scale": _vector(transform.scale3d),
    }
    try:
        record["forward"] = _vector(transform.get_unit_axis(unreal.Axis.X))
        record["right"] = _vector(transform.get_unit_axis(unreal.Axis.Y))
        record["up"] = _vector(transform.get_unit_axis(unreal.Axis.Z))
    except Exception:
        pass

    return record


def _actor_record(actor: Any) -> Dict[str, Any]:
    transform = actor.get_actor_transform()
    transform_record = {
        "position": _vector(transform.translation),
        "rotationDegrees": _rotator(transform.rotation.rotator()),
        "scale": _vector(transform.scale3d),
    }
    try:
        transform_record["forward"] = _vector(transform.get_unit_axis(unreal.Axis.X))
        transform_record["right"] = _vector(transform.get_unit_axis(unreal.Axis.Y))
        transform_record["up"] = _vector(transform.get_unit_axis(unreal.Axis.Z))
    except Exception:
        pass

    return {
        "id": actor.get_path_name(),
        "name": _actor_label(actor),
        "class": _class_name(actor),
        "path": actor.get_path_name(),
        "transform": transform_record,
    }


def _mesh_component_record(
    actor: Any,
    component: Any,
    context: Dict[str, Any]
) -> Optional[Dict[str, Any]]:
    mesh = _safe_editor_property(component, "static_mesh")
    skeletal_mesh = _safe_editor_property(component, "skeletal_mesh")
    source_mesh = mesh if mesh is not None else skeletal_mesh
    if source_mesh is None:
        return None

    materials: List[str] = []
    try:
        material_count = int(component.get_num_materials())
        for index in range(material_count):
            material = component.get_material(index)
            materials.append(_asset_path(material))
    except Exception:
        pass

    bounds = {}
    try:
        origin, extent = component.get_local_bounds()
        bounds = {
            "localMin": _vector(origin),
            "localMax": _vector(extent),
        }
    except Exception:
        pass

    source_asset_id = _asset_path(source_mesh)
    export_record = _export_mesh_asset(source_mesh, context)

    return {
        "id": component.get_path_name(),
        "actorId": actor.get_path_name(),
        "actorName": _actor_label(actor),
        "componentName": component.get_name(),
        "componentClass": _class_name(component),
        "sourceAssetId": source_asset_id,
        "sourceAssetClass": _class_name(source_mesh),
        "materials": materials,
        "transform": _component_world_transform(component),
        "bounds": bounds,
        "exportedPath": export_record.get("exportedPath", ""),
        "plannedExportedPath": export_record.get("plannedExportedPath", ""),
        "exportStatus": export_record.get("exportStatus", "requires_mesh_export"),
        "exportFormat": export_record.get("exportFormat", "fbx"),
        "exportMessage": export_record.get("message", ""),
    }


